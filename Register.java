    private void RegisterjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RegisterjButtonActionPerformed
        player = new Player();
        player.setNationality( NationComBox.getSelectedItem().toString() );
        player.setName(NameText.getText().trim());
        try{
            player.setAge( Integer.parseInt(AgeText.getText().trim()) );
            setVisible(false);
            startGame = new StartGame(player);
            startGame.setVisible(true);
        }
        catch (NumberFormatException e) {
            AgeText.setText("Invalid age");
        }
    }//GEN-LAST:event_RegisterjButtonActionPerformed

